        [[ $? -ne 0 ]] && { rm -f "$_tmpfile"; return 1; }
        _p[TILES]=$(cat "$_tmpfile")

        $DIALOG_TOOL --title "Resize" --inputbox "Environment Name:" 9 60 "${_p[DEFAULT_ENV]:-}" 2>"$_tmpfile"
        [[ $? -ne 0 ]] && { rm -f "$_tmpfile"; return 1; }
        _p[DEFAULT_ENV]=$(cat "$_tmpfile")

        rm -f "$_tmpfile"
    fi

