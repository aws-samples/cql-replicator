# Show a summary + yes/no confirmation, then execute the module function.
# Args: $1 = nameref name, $2 = command label, $3 = module function name, $4 = summary text
_tui_confirm_and_run() {
    local _nameref=$1
    local _label=$2
    local _func=$3
    local _summary=$4
    local _tmpfile
    _tmpfile=$(mktemp /tmp/cqlreplicator-tui-XXXXXX)

    $DIALOG_TOOL --title "Confirm: $_label" \
        --yesno "${_summary}"$'\n\nProceed?' 28 72 \
        2>"$_tmpfile"
    local _rc=$?
    rm -f "$_tmpfile"
    if [[ $_rc -eq 0 ]]; then
        _tui_execute "$_nameref" "$_func" "$_label"
    fi
}

# Run a module function, stream output to a fixed log file, then notify.
# Args: $1 = nameref name, $2 = module function name, $3 = command label
_tui_execute() {
    local _nameref=$1
    local _func=$2
    local _label=$3
    local _logfile="/tmp/cqlreplicator-${_label}.log"

    # Clear terminal so module output is readable, then run
    tput reset 2>/dev/null || true
    printf 'Running %s — output streaming to %s\n\n' "$_label" "$_logfile"

    ( "$_func" "$_nameref" ) 2>&1 | tee "$_logfile"
    local _exit_code=${PIPESTATUS[0]}

    if [[ $_exit_code -ne 0 ]]; then
        printf '\n--- %s exited with code %d ---\n' "$_label" "$_exit_code" | tee -a "$_logfile"
    else
        printf '\n--- %s completed successfully ---\n' "$_label" | tee -a "$_logfile"
    fi

    printf '\nLog saved to: %s\nPress Enter to return to menu...' "$_logfile"
    read -r _
}

