    _tmpfile=$(mktemp /tmp/cqlreplicator-tui-XXXXXX)
    local _tt="${_p[TARGET_TYPE]:-keyspaces}"
    $DIALOG_TOOL --title "Resize — Target Type" \
        --radiolist "Select target type:" 12 55 3 \
        "keyspaces"  "Amazon Keyspaces"    "$( [[ "$_tt" == "keyspaces"  ]] && echo ON || echo OFF )" \
        "dynamodb"   "Amazon DynamoDB"     "$( [[ "$_tt" == "dynamodb"   ]] && echo ON || echo OFF )" \
        "parquet"    "S3 Parquet/Iceberg"  "$( [[ "$_tt" == "parquet"    ]] && echo ON || echo OFF )" \
        2>"$_tmpfile"
    [[ $? -ne 0 ]] && { rm -f "$_tmpfile"; return 1; }
    _p[TARGET_TYPE]=$(cat "$_tmpfile")
    [[ -z "${_p[TARGET_TYPE]}" ]] && _p[TARGET_TYPE]="keyspaces"

    # DynamoDB and Parquet require JSON mapping for resize
    if [[ "${_p[TARGET_TYPE]}" == "dynamodb" ]]; then
        local _jm=""
        while [[ -z "$_jm" ]]; do
            $DIALOG_TOOL --title "Resize — JSON Mapping (required)" \
                --inputbox "JSON mapping (mandatory for DynamoDB):" 10 75 \
                "${_p[JSON_MAPPING_RAW]:-}" \
                2>"$_tmpfile"
