# ── Dialog tool detection ────────────────────────────────────────────────────
# Sets DIALOG_TOOL to "dialog" or "whiptail", exits if neither is available.
_tui_detect_tool() {
    if command -v dialog &>/dev/null; then
        DIALOG_TOOL="dialog"
        # dialog shows an "IMPORTANT MESSAGE" box on first run if ~/.dialogrc is absent.
        # Write a minimal rc file directly to suppress it permanently.
        if [[ ! -f "$HOME/.dialogrc" ]]; then
            printf '# dialogrc generated by CQLReplicator TUI\nuse_colors = ON\n' \
                > "$HOME/.dialogrc" 2>/dev/null || true
        fi
    elif command -v whiptail &>/dev/null; then
        DIALOG_TOOL="whiptail"
    else
        log_error "Neither 'dialog' nor 'whiptail' is installed. Install one to use the TUI."
        exit 1
    fi
}

# ── Main menu ────────────────────────────────────────────────────────────────
# Displays the top-level command menu.
# Writes selected tag to stdout. Returns 1 on Cancel/Escape.
_tui_main_menu() {
    local _tmpfile
    _tmpfile=$(mktemp /tmp/cqlreplicator-menu-XXXXXX)
    $DIALOG_TOOL --title "CQLReplicator" --menu "Select a command:" 20 60 10 \
        "init"            "Deploy CQLReplicator Glue job" \
        "run"             "Start migration process" \
        "stats"           "View migration statistics" \
        "request-stop"    "Stop migration gracefully" \
        "cleanup"         "Delete all artifacts" \
        "kill"            "Stop migration forcefully" \
        "resize"          "Resize tile count" \
        "scheduled-run"   "Schedule as cron job" \
        "get-failed-jobs" "Analyze failed job logs" \
        2>"$_tmpfile"
    local _rc=$?
    printf '%s' "$(cat "$_tmpfile")"
    rm -f "$_tmpfile"
    return $_rc
}

# ── Schema parser ────────────────────────────────────────────────────────────
# Parse a Cassandra/Astra DDL file and extract keyspace and table names.
# Args:
#   $1 = path to the schema DDL file
#   $2 = nameref to an indexed array (keyspace names, deduplicated)
#   $3 = nameref to an associative array (keyspace -> space-separated table names)
# Returns 0 on success, 1 if the file does not exist.
_tui_parse_schema() {
    local _schema_file="$1"
    local -n _ps_ks_arr=$2
    local -n _ps_tbl_map=$3

    if [[ ! -f "$_schema_file" ]]; then
        return 1
    fi

    # Temporary associative array to track seen keyspaces for deduplication
    declare -A _ks_seen=()

    while IFS= read -r _line; do
        # Skip blank lines
        [[ -z "${_line// }" ]] && continue
        # Skip comment lines starting with -- or //
        [[ "$_line" =~ ^[[:space:]]*-- ]] && continue
        [[ "$_line" =~ ^[[:space:]]*// ]] && continue

        # Match CREATE KEYSPACE <name> (case-insensitive)
        if [[ "${_line,,}" =~ ^[[:space:]]*create[[:space:]]+keyspace[[:space:]] ]]; then
            # Extract the name token after CREATE KEYSPACE, handling quoted and unquoted forms
            local _ks_name
            # Try quoted form first: "name" or 'name'
            if echo "$_line" | grep -qE \
                '[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Kk][Ee][Yy][Ss][Pp][Aa][Cc][Ee][[:space:]]+["'"'"'][^"'"'"']+["'"'"']'; then
                _ks_name=$(echo "$_line" | sed -E \
                    's/.*[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Kk][Ee][Yy][Ss][Pp][Aa][Cc][Ee][[:space:]]+["'"'"']([^"'"'"']+)["'"'"'].*/\1/')
            else
                # Unquoted form
                _ks_name=$(echo "$_line" | sed -E \
                    's/^[[:space:]]*[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Kk][Ee][Yy][Ss][Pp][Aa][Cc][Ee][[:space:]]+([^[:space:]"'"'"'(;]+).*/\1/')
            fi
            if [[ -n "$_ks_name" && -z "${_ks_seen[$_ks_name]+x}" ]]; then
                _ps_ks_arr+=( "$_ks_name" )
                _ks_seen["$_ks_name"]=1
            fi
            continue
        fi

        # Match CREATE TABLE <ks>.<tbl> (case-insensitive)
        if [[ "${_line,,}" =~ ^[[:space:]]*create[[:space:]]+table[[:space:]] ]]; then
            # Extract ks and tbl separately, handling both quoted and unquoted forms:
            #   CREATE TABLE ks.tbl (...)
            #   CREATE TABLE "ks"."tbl" (...)
            #   CREATE TABLE 'ks'.'tbl' (...)
            local _ks _tbl
            # Try quoted form first: "ks"."tbl" or 'ks'.'tbl'
            if echo "$_line" | grep -qE \
                '[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Tt][Aa][Bb][Ll][Ee][[:space:]]+["\x27][^"'"'"']+["\x27]\.["\x27][^"'"'"']+["\x27]'; then
                _ks=$(echo "$_line" | sed -E \
                    's/.*[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Tt][Aa][Bb][Ll][Ee][[:space:]]+["'"'"']([^"'"'"']+)["'"'"']\.["'"'"']([^"'"'"']+)["'"'"'].*/\1/')
                _tbl=$(echo "$_line" | sed -E \
                    's/.*[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Tt][Aa][Bb][Ll][Ee][[:space:]]+["'"'"']([^"'"'"']+)["'"'"']\.["'"'"']([^"'"'"']+)["'"'"'].*/\2/')
            else
                # Unquoted form: ks.tbl
                local _kstbl
                _kstbl=$(echo "$_line" | sed -E \
                    's/^[[:space:]]*[Cc][Rr][Ee][Aa][Tt][Ee][[:space:]]+[Tt][Aa][Bb][Ll][Ee][[:space:]]+([^[:space:]"'"'"'(;]+).*/\1/')
                _kstbl="${_kstbl//\"/}"
                _kstbl="${_kstbl//\'/}"
                _ks="${_kstbl%%.*}"
                _tbl="${_kstbl#*.}"
            fi

            # Must have both ks and tbl, and they must differ (i.e. there was a dot)
            if [[ -n "$_ks" && -n "$_tbl" && "$_ks" != "$_tbl" ]]; then
                # Infer keyspace if not already seen
                if [[ -z "${_ks_seen[$_ks]+x}" ]]; then
                    _ps_ks_arr+=( "$_ks" )
                    _ks_seen["$_ks"]=1
                fi
                # Append table to the map (space-separated)
                if [[ -z "${_ps_tbl_map[$_ks]+x}" ]]; then
                    _ps_tbl_map["$_ks"]="$_tbl"
                else
                    _ps_tbl_map["$_ks"]+=" $_tbl"
                fi
            fi
            continue
        fi
    done < "$_schema_file"

    return 0
}

# ── Target resolver ─────────────────────────────────────────────────────────
# Query AWS APIs to enumerate target keyspaces or tables.
# Args:
#   $1 = target_type  : "keyspaces" | "dynamodb" | "parquet"
#   $2 = aws_region   : AWS region string (e.g. "us-east-1")
#   $3 = ks_name_or_empty : keyspace name (for keyspaces list-tables) or empty
# Prints one name per line to stdout.
# Returns 0 on success, 1 on AWS CLI / jq failure.
_tui_resolve_target_list() {
    local _target_type="$1"
    local _aws_region="$2"
    local _ks_name="${3:-}"

    case "$_target_type" in
        keyspaces)
            if [[ -z "$_ks_name" ]]; then
                # List all keyspaces
                local _json
                _json=$(aws keyspaces list-keyspaces --region "$_aws_region" 2>/dev/null) || return 1
                printf '%s' "$_json" | jq -r '.keyspaces[].keyspaceName' 2>/dev/null || return 1
            else
                # List tables in the given keyspace
                local _json
                _json=$(aws keyspaces list-tables --keyspace-name "$_ks_name" --region "$_aws_region" 2>/dev/null) || return 1
                printf '%s' "$_json" | jq -r '.tables[].tableName' 2>/dev/null || return 1
            fi
            ;;
        dynamodb)
            local _json
            _json=$(aws dynamodb list-tables --region "$_aws_region" 2>/dev/null) || return 1
            printf '%s' "$_json" | jq -r '.TableNames[]' 2>/dev/null || return 1
            ;;
        parquet)
            # No AWS calls for parquet; caller uses inputbox
            return 0
            ;;
        *)
            return 1
            ;;
    esac
}

# ── Schema-driven selection screen ───────────────────────────────────────────
# Drives the 4-step source/target selection flow using parsed schema data.
# Args: $1 = nameref name (string) for the params associative array
# Returns 0 on success (params populated), 1 on cancel or error.
_tui_schema_selection() {
    local -n _p=$1
    local _tmpfile
    _tmpfile=$(mktemp /tmp/cqlreplicator-schema-XXXXXX)

    # Step 0: Parse the schema file
    local -a _ks_list=()
    local -A _tbl_map=()
    if ! _tui_parse_schema "${_p[SCHEMA_FILE]}" _ks_list _tbl_map; then
        $DIALOG_TOOL --title "Schema Error" \
            --msgbox "Schema file not found: ${_p[SCHEMA_FILE]}" 8 60
        rm -f "$_tmpfile"
        return 1
    fi

    # If schema is empty (no keyspaces/tables found), inform user and return 1
    if [[ ${#_ks_list[@]} -eq 0 ]]; then
        $DIALOG_TOOL --title "Schema Info" \
            --msgbox "No keyspaces or tables found in schema file.\nProceeding with manual entry." 8 60
        rm -f "$_tmpfile"
        return 1
    fi

    local _src_ks="" _src_tbl="" _tgt_ks="" _tgt_tbl=""
    local _step=0

    while true; do
        case $_step in
        0)
            # ── Step 0.5: Target type selection ──
            local _tt="${_p[TARGET_TYPE]:-keyspaces}"
            $DIALOG_TOOL --title "Target Type" \
                --radiolist "Select target type:" 12 60 3 \
                "keyspaces"  "Amazon Keyspaces"    "$( [[ "$_tt" == "keyspaces"  ]] && echo ON || echo OFF )" \
                "dynamodb"   "Amazon DynamoDB"     "$( [[ "$_tt" == "dynamodb"   ]] && echo ON || echo OFF )" \
                "parquet"    "Amazon S3"           "$( [[ "$_tt" == "parquet"    ]] && echo ON || echo OFF )" \
                2>"$_tmpfile"
            if [[ $? -ne 0 ]]; then
                rm -f "$_tmpfile"
                return 1
            fi
            _p[TARGET_TYPE]=$(cat "$_tmpfile")
            [[ -z "${_p[TARGET_TYPE]}" ]] && _p[TARGET_TYPE]="keyspaces"
            _step=1
            ;;
        1)
            # ── Step 1: Source keyspace menu ──
            local -a _ks_menu_items=()
            local _k
            for _k in "${_ks_list[@]}"; do
                _ks_menu_items+=( "$_k" "Keyspace: $_k" )
            done

            $DIALOG_TOOL --title "Source Keyspace" \
                --menu "Select source keyspace:" 20 60 10 \
                "${_ks_menu_items[@]}" \
                2>"$_tmpfile"
            if [[ $? -ne 0 ]]; then
                _step=0
                continue
            fi
            _src_ks=$(cat "$_tmpfile")
            _step=2
            ;;
        2)
            # ── Step 2: Source table menu ──
            local _tables_str="${_tbl_map[$_src_ks]:-}"
            if [[ -z "$_tables_str" ]]; then
                $DIALOG_TOOL --title "Source Table" \
                    --msgbox "No tables found for keyspace: $_src_ks" 8 60
                _step=1
                continue
            fi

            local -a _tbl_menu_items=()
            local _t
            for _t in $_tables_str; do
                _tbl_menu_items+=( "$_t" "Table: $_t" )
            done

            $DIALOG_TOOL --title "Source Table" \
                --menu "Select source table (keyspace: $_src_ks):" 20 60 10 \
                "${_tbl_menu_items[@]}" \
                2>"$_tmpfile"
            if [[ $? -ne 0 ]]; then
                _step=1
                continue
            fi
            _src_tbl=$(cat "$_tmpfile")
            _step=3
            ;;
        3)
            # ── Step 3: Target keyspace ──
            local _tt="${_p[TARGET_TYPE]:-keyspaces}"

            if [[ "$_tt" == "dynamodb" ]]; then
                # DynamoDB has no keyspace concept — skip to target table
                _tgt_ks="None"
                _step=4
                continue
            fi

            if [[ "$_tt" == "parquet" ]]; then
                # Parquet — collect S3 bucket, prefix, maxFileSizeMb for JSON mapping
                # No concept of target keyspace/table; build json-mapping instead
                local _s3_bucket="" _s3_prefix="" _s3_max_file=""

                $DIALOG_TOOL --title "Parquet — S3 Bucket" \
                    --inputbox "Enter S3 bucket name:" 8 60 "" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=0
                    continue
                fi
                _s3_bucket=$(cat "$_tmpfile")

                $DIALOG_TOOL --title "Parquet — S3 Prefix" \
                    --inputbox "Enter S3 prefix (e.g. snapshots):" 8 60 "" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=0
                    continue
                fi
                _s3_prefix=$(cat "$_tmpfile")

                $DIALOG_TOOL --title "Parquet — Max File Size" \
                    --inputbox "Max file size in MB:" 8 60 "32" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=0
                    continue
                fi
                _s3_max_file=$(cat "$_tmpfile")
                [[ -z "$_s3_max_file" ]] && _s3_max_file="32"

                # Build JSON mapping for parquet/S3
                _p[JSON_MAPPING_RAW]=$(printf '{"s3":{"bucket":"%s","prefix":"%s","maxFileSizeMb":%s}}' \
                    "$_s3_bucket" "$_s3_prefix" "$_s3_max_file")
                _tgt_ks="$_s3_bucket"
                _tgt_tbl="$_s3_prefix"

                # Parquet is done — skip Step 4
                break
            fi

            # keyspaces target type — resolve via AWS
            # Always prompt for AWS_REGION so user can change it on back-navigation
            $DIALOG_TOOL --title "AWS Region" \
                --inputbox "Enter AWS Region:" 8 60 "${_p[AWS_REGION]:-us-east-1}" \
                2>"$_tmpfile"
            if [[ $? -ne 0 ]]; then
                _step=0
                continue
            fi
            _p[AWS_REGION]=$(cat "$_tmpfile")

            local _tgt_ks_names
            _tgt_ks_names=$(_tui_resolve_target_list "keyspaces" "${_p[AWS_REGION]}" "")
            if [[ $? -ne 0 || -z "$_tgt_ks_names" ]]; then
                # Fallback to inputbox on AWS failure or empty list
                $DIALOG_TOOL --title "Target Keyspace" \
                    --inputbox "Enter target keyspace (AWS lookup failed):" 8 60 "" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=0
                    continue
                fi
                _tgt_ks=$(cat "$_tmpfile")
            else
                # Build menu from AWS results
                local -a _tgt_ks_menu=()
                local _name
                while IFS= read -r _name; do
                    [[ -n "$_name" ]] && _tgt_ks_menu+=( "$_name" "Keyspace: $_name" )
                done <<< "$_tgt_ks_names"

                $DIALOG_TOOL --title "Target Keyspace" \
                    --menu "Select target keyspace:" 20 60 10 \
                    "${_tgt_ks_menu[@]}" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=0
                    continue
                fi
                _tgt_ks=$(cat "$_tmpfile")
            fi
            _step=4
            ;;
        4)
            # ── Step 4: Target table ──
            local _tt="${_p[TARGET_TYPE]:-keyspaces}"

            # Always prompt for AWS_REGION so user can change it on back-navigation
            $DIALOG_TOOL --title "AWS Region" \
                --inputbox "Enter AWS Region:" 8 60 "${_p[AWS_REGION]:-us-east-1}" \
                2>"$_tmpfile"
            if [[ $? -ne 0 ]]; then
                _step=0
                continue
            fi
            _p[AWS_REGION]=$(cat "$_tmpfile")

            local _tgt_tbl_names
            if [[ "$_tt" == "keyspaces" ]]; then
                _tgt_tbl_names=$(_tui_resolve_target_list "keyspaces" "${_p[AWS_REGION]}" "$_tgt_ks")
            else
                # dynamodb
                _tgt_tbl_names=$(_tui_resolve_target_list "dynamodb" "${_p[AWS_REGION]}" "")
            fi

            if [[ $? -ne 0 || -z "$_tgt_tbl_names" ]]; then
                # Fallback to inputbox on AWS failure or empty list
                $DIALOG_TOOL --title "Target Table" \
                    --inputbox "Enter target table (AWS lookup failed):" 8 60 "" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=3
                    continue
                fi
                _tgt_tbl=$(cat "$_tmpfile")
            else
                # Build menu from AWS results
                local -a _tgt_tbl_menu=()
                local _name
                while IFS= read -r _name; do
                    [[ -n "$_name" ]] && _tgt_tbl_menu+=( "$_name" "Table: $_name" )
                done <<< "$_tgt_tbl_names"

                $DIALOG_TOOL --title "Target Table" \
                    --menu "Select target table:" 20 60 10 \
                    "${_tgt_tbl_menu[@]}" \
                    2>"$_tmpfile"
                if [[ $? -ne 0 ]]; then
                    _step=3
                    continue
                fi
                _tgt_tbl=$(cat "$_tmpfile")
            fi

            # DynamoDB requires mandatory JSON mapping — auto-populate from table schema
            if [[ "$_tt" == "dynamodb" ]]; then
                # Describe the selected table to get key schema
                local _pk_name="pk" _sk_name="" _describe_json=""
                _describe_json=$(aws dynamodb describe-table \
                    --table-name "$_tgt_tbl" \
                    --region "${_p[AWS_REGION]}" 2>/dev/null) || true

                if [[ -n "$_describe_json" ]]; then
                    _pk_name=$(printf '%s' "$_describe_json" | \
                        jq -r '.Table.KeySchema[] | select(.KeyType=="HASH") | .AttributeName' 2>/dev/null) || true
                    _sk_name=$(printf '%s' "$_describe_json" | \
                        jq -r '.Table.KeySchema[] | select(.KeyType=="RANGE") | .AttributeName' 2>/dev/null) || true
                    [[ "$_pk_name" == "null" || -z "$_pk_name" ]] && _pk_name="pk"
                    [[ "$_sk_name" == "null" ]] && _sk_name=""
                fi

                # Build endpoint from region
                local _ddb_endpoint="dynamodb.${_p[AWS_REGION]}.amazonaws.com"

                # Pre-populate JSON mapping
                local _auto_jm
                if [[ -n "$_sk_name" ]]; then
                    _auto_jm=$(printf '{"replication":{"dynamoDBPrimaryKey":{"partitionKeyName":"%s","sortKeyName":"%s","separator":"#"}},"dynamodb":{"dynamoDBConnection":{"endpoint":"%s","region":"%s"}}}' \
                        "$_pk_name" "$_sk_name" "$_ddb_endpoint" "${_p[AWS_REGION]}")
                else
                    _auto_jm=$(printf '{"replication":{"dynamoDBPrimaryKey":{"partitionKeyName":"%s","sortKeyName":"","separator":"#"}},"dynamodb":{"dynamoDBConnection":{"endpoint":"%s","region":"%s"}}}' \
                        "$_pk_name" "$_ddb_endpoint" "${_p[AWS_REGION]}")
                fi

                # Show pre-filled JSON mapping for user to review/edit
                local _jm=""
                while [[ -z "$_jm" ]]; do
                    $DIALOG_TOOL --title "DynamoDB — JSON Mapping (required)" \
                        --inputbox "Review/edit JSON mapping:" 10 75 \
                        "$_auto_jm" \
                        2>"$_tmpfile"
                    if [[ $? -ne 0 ]]; then
                        _step=3
                        continue 2
                    fi
                    _jm=$(cat "$_tmpfile")
                    if [[ -z "$_jm" ]]; then
                        $DIALOG_TOOL --title "Error" \
                            --msgbox "JSON mapping is required for DynamoDB target. Cannot proceed without it." 8 60
                    fi
                done
                _p[JSON_MAPPING_RAW]="$_jm"
            fi

            break
            ;;
        esac
    done

    # Store results
    _p[SOURCE_KS]="$_src_ks"
    _p[SOURCE_TBL]="$_src_tbl"
    _p[TARGET_KS]="$_tgt_ks"
    _p[TARGET_TBL]="$_tgt_tbl"

    rm -f "$_tmpfile"
    return 0
}

# ── Entry point ──────────────────────────────────────────────────────────────
# Args: $1 = nameref to params associative array
module_tui() {
    local -n _params=$1
    _tui_detect_tool

    # Pre-load S3 landing zone from working_bucket.dat if present and not already set
    local _bucket_file="${SCRIPT_DIR}/working_bucket.dat"
    if [[ -z "${_params[S3_LANDING_ZONE]:-}" && -f "$_bucket_file" ]]; then
        local _bucket
        _bucket=$(tr -d '[:space:]' < "$_bucket_file")
        [[ -n "$_bucket" ]] && _params[S3_LANDING_ZONE]="$_bucket"
    fi

    # Plan mode: delegate to module_plan for multi-table migration plan wizard
    # Check BEFORE schema-driven selection so plan mode takes priority
    if [[ "${_params[PLAN_MODE]:-}" == "true" && -n "${_params[SCHEMA_FILE]:-}" ]]; then
        module_plan "$1"
        return $?
    fi

    # Schema-driven pre-selection: invoke selection screen before main menu
    if [[ -n "${_params[SCHEMA_FILE]:-}" ]]; then
        _tui_schema_selection "$1" || { tput reset 2>/dev/null || true; return 0; }
    fi

    # Migration plan name: delegate to plan operations menu (Run All / Stop All / Kill All)
    if [[ -n "${_params[MIGRATION_PLAN_NAME]:-}" ]]; then
        _plan_operations_menu "$1"
        return $?
    fi

    local _cmd _tmpfile
    _tmpfile=$(mktemp /tmp/cqlreplicator-tui-XXXXXX)

    while true; do
        $DIALOG_TOOL --title "CQLReplicator" --menu "Select a command:" 20 60 10 \
            "init"            "Deploy CQLReplicator Glue job" \
            "run"             "Start migration process" \
            "stats"           "View migration statistics" \
            "request-stop"    "Stop migration gracefully" \
            "cleanup"         "Delete all artifacts" \
            "kill"            "Stop migration forcefully" \
            "resize"          "Resize tile count" \
            "scheduled-run"   "Schedule as cron job" \
            "get-failed-jobs" "Analyze failed job logs" \
            2>"$_tmpfile" || break   # Cancel/Escape exits

        _cmd=$(cat "$_tmpfile")

        case "$_cmd" in
            init)            _tui_wizard_init "$1" ;;
            run)             _tui_wizard_run "$1" ;;
            stats)           _tui_wizard_stats "$1" ;;
            request-stop)    _tui_wizard_request_stop "$1" ;;
            cleanup)         _tui_wizard_cleanup "$1" ;;
            kill)            _tui_wizard_kill "$1" ;;
            resize)          _tui_wizard_resize "$1" ;;
            scheduled-run)   _tui_wizard_scheduled_run "$1" ;;
            get-failed-jobs) _tui_wizard_get_failed_jobs "$1" ;;
        esac
    done

    rm -f "$_tmpfile"
    # Restore terminal state
    tput reset 2>/dev/null || reset 2>/dev/null || true
}

# ── Shared helpers ───────────────────────────────────────────────────────────

# Normalize a security-group string entered by the user into the format
# required by the Glue connection JSON: "sg-aaa","sg-bbb"
# Input:  sg-aaa,sg-bbb  OR  "sg-aaa","sg-bbb"  OR  already-quoted form
# Output: "sg-aaa","sg-bbb"  (stored back into the named variable)
# Args: $1 = nameref name of the associative array, $2 = key name (e.g. SG)
_tui_normalize_sg() {
    local -n _arr=$1
    local _key=$2
    local _raw="${_arr[$_key]:-}"
    [[ -z "$_raw" ]] && return 0

    # Strip any existing outer single-quotes and inner double-quotes, then split on comma
    local _stripped
    _stripped=$(printf '%s' "$_raw" | sed "s/^'//;s/'$//" | tr -d '"')

    local _normalized="" _id
    IFS=',' read -ra _ids <<< "$_stripped"
    for _id in "${_ids[@]}"; do
        _id="${_id// /}"   # trim spaces
        [[ -z "$_id" ]] && continue
        [[ -n "$_normalized" ]] && _normalized+=","
        _normalized+="\"${_id}\""
    done

    _arr[$_key]="${_normalized}"
}

# Collect source/target table + S3 zone + region via form or sequential inputboxes.
# Args: $1 = nameref name (string) for the params associative array
# Returns 1 on Cancel.
_tui_collect_source_target() {
    local -n _p=$1
    local _tmpfile
    _tmpfile=$(mktemp /tmp/cqlreplicator-tui-XXXXXX)

    if [[ "$DIALOG_TOOL" == "dialog" ]]; then
        $DIALOG_TOOL --title "Source & Target" \
            --form "Enter source and target details:" 16 65 6 \
            "Source Keyspace:" 1 1 "${_p[SOURCE_KS]:-}"        1 22 35 0 \
            "Source Table:"    2 1 "${_p[SOURCE_TBL]:-}"       2 22 35 0 \
