                    --msgbox "JSON mapping is required for DynamoDB target." 8 60
            fi
        done
        _p[JSON_MAPPING_RAW]="$_jm"
    elif [[ "${_p[TARGET_TYPE]}" == "parquet" ]]; then
        $DIALOG_TOOL --title "Resize — JSON Mapping" \
            --inputbox "JSON mapping (optional for Parquet):" 10 75 \
            "${_p[JSON_MAPPING_RAW]:-}" \
            2>"$_tmpfile"
        [[ $? -ne 0 ]] && { rm -f "$_tmpfile"; return 1; }
        _p[JSON_MAPPING_RAW]=$(cat "$_tmpfile")
    fi

    # Apply JSON mapping if provided
    if [[ -n "${_p[JSON_MAPPING_RAW]:-}" ]]; then
        JSON_MAPPING="${_p[JSON_MAPPING_RAW]}"
        if command -v base64 &>/dev/null; then
            JSON_MAPPING_B64=$(printf '%s' "$JSON_MAPPING" | base64 -w 0 2>/dev/null || printf '%s' "$JSON_MAPPING" | base64)
        fi
    fi
    rm -f "$_tmpfile"

    # Warning: replication must be stopped first
    _tmpfile=$(mktemp /tmp/cqlreplicator-tui-XXXXXX)
    $DIALOG_TOOL --title "Resize - Warning" \
        --yesno "IMPORTANT: The replication process for"$'\n\n'"  ${_p[SOURCE_KS]}.${_p[SOURCE_TBL]}"$'\n\n'"must be STOPPED before resizing."$'\n\n'"Have you already stopped the replication?" \
        13 72 \
        2>"$_tmpfile"
    local _rc=$?
    rm -f "$_tmpfile"
    [[ $_rc -ne 0 ]] && return 0

    local _summary
    _summary="Source:    ${_p[SOURCE_KS]}.${_p[SOURCE_TBL]}
S3 Zone:   ${_p[S3_LANDING_ZONE]}
Region:    ${_p[AWS_REGION]}
Target:    ${_p[TARGET_TYPE]}
New Tiles: ${_p[TILES]}
Env Name:  ${_p[DEFAULT_ENV]}
JSON Map:  ${_p[JSON_MAPPING_RAW]:-(none)}"

    _tui_confirm_and_run "$1" "resize" "module_resize" "$_summary"
}

